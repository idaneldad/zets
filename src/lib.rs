/// Language identifier — ISO 639-1 2-letter lowercase code.
///
/// Stored as 2 bytes, always `Copy`. Comparable in a single `cmp` instruction.
///
/// # Examples
///
/// ```ignore
/// let he = LangCode::HEBREW;
/// let parsed = LangCode::parse("HE").unwrap();  // case-insensitive
/// assert_eq!(he, parsed);
/// ```
#[derive(Clone, Copy, PartialEq, Eq, Hash, PartialOrd, Ord)]
pub struct LangCode([u8; 2]);

impl LangCode {
    pub const HEBREW: LangCode = LangCode(*b"he");
    pub const ENGLISH: LangCode = LangCode(*b"en");
    pub const ARABIC: LangCode = LangCode(*b"ar");
    pub const CHINESE: LangCode = LangCode(*b"cn");
    pub const RUSSIAN: LangCode = LangCode(*b"ru");
    pub const JAPANESE: LangCode = LangCode(*b"ja");
    pub const SPANISH: LangCode = LangCode(*b"es");
    pub const FRENCH: LangCode = LangCode(*b"fr");
    pub const GERMAN: LangCode = LangCode(*b"de");
    pub const PORTUGUESE: LangCode = LangCode(*b"pt");

    /// Parse a 2-letter ASCII code, case-insensitive. Returns `None` if
    /// the input is not exactly 2 ASCII alphabetic characters.
    #[inline]
    pub const fn parse(s: &str) -> Option<Self> {
        let b = s.as_bytes();
        if b.len() != 2 {
            return None;
        }
        let b0 = b[0].to_ascii_lowercase();
        let b1 = b[1].to_ascii_lowercase();
        if !b0.is_ascii_lowercase() || !b1.is_ascii_lowercase() {
            return None;
        }
        Some(Self([b0, b1]))
    }

    /// Internal representation as two bytes.
    #[inline]
    pub const fn as_bytes(self) -> [u8; 2] {
        self.0
    }

    /// As a string slice.
    #[inline]
    pub fn as_str(&self) -> &str {
        // Constructors guarantee valid ASCII, so UTF-8 validity is trivial.
        std::str::from_utf8(&self.0).unwrap_or("??")
    }

    /// Look up the language metadata from the compile-time table.
    /// Returns `None` if the code is not in the supported list.
    pub fn meta(self) -> Option<&'static LanguageMeta> {
        LANGUAGES.iter().find(|m| m.code.as_bytes() == self.0)
    }

    /// True if the language is right-to-left. Returns false for unknown codes.
    pub fn is_rtl(self) -> bool {
        self.meta().is_some_and(|m| m.rtl)
    }
}

impl fmt::Debug for LangCode {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "LangCode({})", self.as_str())
    }
}

impl fmt::Display for LangCode {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        f.write_str(self.as_str())
    }
}

/// A canonical form of text. Opaque wrapper that prevents mixing raw and
/// normalized text at the type level. Constructed only via [`unp::normalize`].
#[derive(Debug, Clone, PartialEq, Eq, Hash)]
pub struct Canonical {
    bytes: Vec<u8>,
    lang: LangCode,
}

impl Canonical {
    /// Private — only UNP can construct these.
    fn new(s: String, lang: LangCode) -> Self {
        Self { bytes: s.into_bytes(), lang }
    }

    #[inline]
    pub fn as_bytes(&self) -> &[u8] {
        &self.bytes
    }

    /// Safe because UNP only produces valid UTF-8.
    #[inline]
    pub fn as_str(&self) -> &str {
        std::str::from_utf8(&self.bytes).expect("Canonical is always valid UTF-8")
    }

    #[inline]
    pub const fn language(&self) -> LangCode {
        self.lang
    }

    #[inline]
    pub fn len(&self) -> usize {
        self.bytes.len()
    }

    #[inline]
    pub fn is_empty(&self) -> bool {
        self.bytes.is_empty()
    }
}

/// What kind of content are we identifying? Determines the identity scheme.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ContentKind {
    Word,
    Phrase,
    Sentence,
    Document,
}

/// Synset identifier. Stored as u24 on disk (3 bytes), u32 in memory.
///
/// Reserved ranges:
/// - `0..1000` — system/meta synsets (languages, relations, permissions)
/// - `1000..10000` — reserved for future system use
/// - `10000..` — user-facing content
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, PartialOrd, Ord)]
pub struct SynsetId(pub u32);

impl SynsetId {
    pub const SYSTEM_ROOT: SynsetId = SynsetId(0);
    pub const LANGUAGE_REGISTRY: SynsetId = SynsetId(1);
    pub const RELATION_REGISTRY: SynsetId = SynsetId(2);
    pub const PERMISSION_REGISTRY: SynsetId = SynsetId(3);
    pub const STATUS_REGISTRY: SynsetId = SynsetId(4);
    pub const USER_REGISTRY: SynsetId = SynsetId(5);

    /// First synset ID available for user content.
    pub const USER_CONTENT_START: u32 = 10_000;

    /// Maximum representable value (24-bit).
    pub const MAX: u32 = 0x00FF_FFFF;

    #[inline]
    pub const fn is_system(self) -> bool {
        self.0 < 1000
    }

    #[inline]
    pub const fn is_user(self) -> bool {
        self.0 >= Self::USER_CONTENT_START
    }
}

/// The identity of a piece of content in the ZETS graph.
#[derive(Debug, Clone)]
pub enum Identity {
    Word(Canonical),
    Phrase(Vec<SynsetId>),
    Hash128([u8; 16]),
    Hash256([u8; 32]),
}

/// How much work to do for a single query.
///
/// This is the user-facing throttle that controls walk depth and
/// confidence targets. Selection is always deterministic given the
/// same query; only the budget differs.
///
/// | Mode       | Walk passes | Max depth | Target p50 latency | Use case                    |
/// |------------|-------------|-----------|-------------------|-----------------------------|
/// | Fast       | 1           | 2         | <50 ms            | factoid lookups             |
/// | Standard   | 1-3         | 3         | <100 ms           | explanations (default)      |
/// | Deliberate | 3-7         | 5         | <2 s              | complex / creative          |
/// | Deep       | async       | unbounded | no latency bound  | background / "think on it"  |
///
/// All modes stop early if confidence >= 0.95.
/// Cap on passes is a hard ceiling per SPEC V6.1 §IV.2.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum ResponseMode {
    Fast,
    Standard,
    Deliberate,
    Deep,
}

impl Default for ResponseMode {
    fn default() -> Self { Self::Standard }
}

impl ResponseMode {
    /// Maximum number of forward+backward passes to run.
    #[inline]
    #[must_use]
    pub const fn max_passes(self) -> u8 {
        match self {
            Self::Fast => 1,
            Self::Standard => 3,
            Self::Deliberate => 7,
            Self::Deep => u8::MAX,  // effectively unbounded
        }
    }

    /// Maximum walk depth from the seed synset.
    #[inline]
    #[must_use]
    pub const fn max_depth(self) -> u8 {
        match self {
            Self::Fast => 2,
            Self::Standard => 3,
            Self::Deliberate => 5,
            Self::Deep => u8::MAX,
        }
    }

    /// Confidence threshold for early termination.
    #[inline]
    #[must_use]
    pub const fn confidence_target(self) -> u8 {
        // 0-100 integer scale (avoid f64 in const fn).
        match self {
            Self::Fast => 80,
            Self::Standard => 90,
            Self::Deliberate => 95,
            Self::Deep => 99,
        }
    }

    /// Human-readable label for logging and UI.
    #[must_use]
    pub const fn label(self) -> &'static str {
        match self {
            Self::Fast => "fast",
            Self::Standard => "standard",
            Self::Deliberate => "deliberate",
            Self::Deep => "deep",
        }
    }
}

