// HEBREW — character classification, niqud, stemmer
// ============================================================================

pub mod hebrew {
    //! Hebrew-specific text processing.

    /// Is this character niqud (vowel point) or cantillation mark?
    ///
    /// Ranges: U+0591..U+05AF (cantillation), U+05B0..U+05BD (points),
    /// U+05BF (rafe), U+05C1..U+05C2 (sin/shin dots),
    /// U+05C4..U+05C5 (meteg/hiriq marks), U+05C7 (holam haser).
    #[inline]
    #[must_use]
    pub const fn is_niqud(c: char) -> bool {
        let code = c as u32;
        matches!(code, 0x0591..=0x05AF | 0x05B0..=0x05BD | 0x05BF
            | 0x05C1..=0x05C2 | 0x05C4..=0x05C5 | 0x05C7)
    }

    /// Geresh (U+05F3) and gershayim (U+05F4).
    #[inline]
    #[must_use]
    pub const fn is_hebrew_punctuation(c: char) -> bool {
        matches!(c as u32, 0x05F3 | 0x05F4)
    }

    /// Is this a Hebrew letter (including final forms)?
    #[inline]
    #[must_use]
    pub const fn is_hebrew_letter(c: char) -> bool {
        matches!(c as u32, 0x05D0..=0x05EA)
    }

    /// Convert final-form letter to regular form.
    /// ך→כ, ם→מ, ן→נ, ף→פ, ץ→צ. Everything else unchanged.
    #[inline]
    #[must_use]
    pub const fn normalize_final_form(c: char) -> char {
        match c {
            'ך' => 'כ',
            'ם' => 'מ',
            'ן' => 'נ',
            'ף' => 'פ',
            'ץ' => 'צ',
            _ => c,
        }
    }

    /// Minimum root length to preserve during stemming.
    pub const MIN_ROOT_CHARS: usize = 3;

    /// Strip niqud and cantillation marks.
    #[must_use]
    pub fn strip_niqud(s: &str) -> String {
        s.chars().filter(|&c| !is_niqud(c)).collect()
    }

    /// Safe Hebrew canonicalization — only normalize final forms.
    ///
    /// Does NOT strip prefixes or suffixes; that requires a dictionary
    /// (Week 3+) because naive stripping over-strips common words.
    #[must_use]
    pub fn canonicalize(word: &str) -> String {
        let word = word.trim();
        if word.is_empty() {
            return String::new();
        }
        // Only process purely-Hebrew words; pass others through.
        if !word.chars().all(|c| is_hebrew_letter(c) || is_hebrew_punctuation(c)) {
            return word.to_string();
        }
        word.chars().map(normalize_final_form).collect()
    }

    /// Aggressive rules-based stemmer — Week 3+ fallback only.
    ///
    /// Uses prefix/suffix tables from `build.rs`. Strips one longest-matching
    /// prefix and one longest-matching suffix, preserving minimum root length.
    ///
    /// Use only when dictionary lookup misses. Will over-strip common words
    /// whose first letter happens to be a prefix letter.
    #[must_use]
    pub fn aggressive_stem_fallback(word: &str) -> String {
        let word = word.trim();
        if word.is_empty() {
            return String::new();
        }
        if !word.chars().all(|c| is_hebrew_letter(c) || is_hebrew_punctuation(c)) {
            return word.to_string();
        }

        let mut stem: String = word.chars().map(normalize_final_form).collect();

        for prefix in super::HEBREW_PREFIXES {
            let p_chars = prefix.chars().count();
            let s_chars = stem.chars().count();
            if s_chars >= p_chars + MIN_ROOT_CHARS && stem.starts_with(prefix) {
                stem = stem[prefix.len()..].to_string();
                break;
            }
        }

        for suffix in super::HEBREW_SUFFIXES {
            let sf_chars = suffix.chars().count();
            let s_chars = stem.chars().count();
            if s_chars >= sf_chars + MIN_ROOT_CHARS && stem.ends_with(suffix) {
                stem.truncate(stem.len() - suffix.len());
                break;
            }
        }

        stem
    }
}

// ============================================================================
// UNP — Unified Normalization Pipeline
// ============================================================================

pub mod unp {
    //! Single source of truth for text normalization. All device instances
    //! MUST produce bit-identical canonical output for identical input.

    use super::{hebrew, Canonical, ContentKind, Identity, LangCode, SynsetId};

    /// Normalize raw text to canonical form.
    #[must_use]
    pub fn normalize(raw: &str, lang: LangCode) -> Canonical {
        let step1 = nfc(raw);
        let step2 = step1.trim().to_string();
        let step3 = collapse_whitespace(&step2);
        let step4 = strip_language_specific(&step3, lang);
        let step5 = lemmatize(&step4, lang);
        Canonical::new(step5, lang)
    }

    /// Identify content with its type-appropriate identity scheme.
    #[must_use]
    pub fn identify(raw: &str, lang: LangCode, kind: ContentKind) -> Identity {
        let canon = normalize(raw, lang);
        match kind {
            ContentKind::Word => Identity::Word(canon),
            ContentKind::Phrase => Identity::Phrase(tokenize_phrase(&canon)),
            ContentKind::Sentence => Identity::Hash128(fnv_128_with_lang(canon.as_bytes(), lang)),
            ContentKind::Document => Identity::Hash256(fnv_256_with_lang(canon.as_bytes(), lang)),
        }
    }

    /// Unicode NFC normalization.
    ///
    /// Week 1-2: pass-through. Modern corpora are typically already NFC.
    /// Week 3+: hand-rolled lookup table for our character ranges (~20KB).
    #[inline]
    fn nfc(s: &str) -> String {
        s.to_string()
    }

    /// Collapse all whitespace runs to single ASCII spaces.
    fn collapse_whitespace(s: &str) -> String {
        let mut out = String::with_capacity(s.len());
        let mut last_was_space = false;
        for c in s.chars() {
            if c.is_whitespace() {
                if !last_was_space {
                    out.push(' ');
                    last_was_space = true;
                }
            } else {
                out.push(c);
                last_was_space = false;
            }
        }
        out
    }

    fn strip_language_specific(s: &str, lang: LangCode) -> String {
        if lang == LangCode::HEBREW {
            hebrew::strip_niqud(s)
        } else if lang == LangCode::ARABIC {
            strip_tatweel(s)
        } else if lang == LangCode::ENGLISH {
            s.to_lowercase()
        } else {
            // Unknown or unhandled language — no language-specific changes.
            s.to_string()
        }
    }

    fn strip_tatweel(s: &str) -> String {
        s.chars().filter(|&c| c != '\u{0640}').collect()
    }

    fn lemmatize(s: &str, lang: LangCode) -> String {
        if lang == LangCode::HEBREW {
            // Week 1-2: canonicalization only. Week 3+ adds dictionary lookup,
            // with `aggressive_stem_fallback` as tail-case.
            hebrew::canonicalize(s)
        } else {
            s.to_string()
        }
    }

    /// Placeholder: tokenize a phrase into synset IDs via dictionary lookup.
    /// Week 3+ implementation; currently returns empty.
    #[inline]
    fn tokenize_phrase(_canon: &Canonical) -> Vec<SynsetId> {
        Vec::new()
    }

    // --- Hash placeholders (FNV-1a, not cryptographic) ---
    //
    // Week 3 replaces with BLAKE3 (via crate; security-critical).
    //
    // IMPORTANT: `_with_lang` variants mix the 2-byte language code into the
    // hash seed. This guarantees that homographs across languages produce
    // DIFFERENT hashes. Example: "gift"[EN] (present) vs "gift"[DE] (poison) —
    // same bytes, different meaning, must have different synset/hash.

    const FNV_OFFSET_128: u128 = 0x6c62_272e_07bb_0142_62b8_2175_6295_c58d;
    const FNV_PRIME_128: u128 = 0x0000_0000_0100_0000_0000_0000_0000_013b;

    /// FNV-1a 128-bit with language code mixed into the seed.
    ///
    /// Layout of input: `[lang_byte_0, lang_byte_1, 0x1F, data...]`
    /// The `0x1F` separator (ASCII unit-separator) prevents collisions between
    /// `("en", "gift")` and `("e", "ngift")`.
    #[inline]
    fn fnv_128_with_lang(data: &[u8], lang: super::LangCode) -> [u8; 16] {
        let lang_bytes = lang.as_bytes();
        let mut h = FNV_OFFSET_128;
        h ^= u128::from(lang_bytes[0]);
        h = h.wrapping_mul(FNV_PRIME_128);
        h ^= u128::from(lang_bytes[1]);
        h = h.wrapping_mul(FNV_PRIME_128);
        h ^= u128::from(0x1Fu8); // unit separator
        h = h.wrapping_mul(FNV_PRIME_128);
        for &b in data {
            h ^= u128::from(b);
            h = h.wrapping_mul(FNV_PRIME_128);
        }
        h.to_le_bytes()
    }

    /// FNV-1a 256-bit with language code mixed in.
    #[inline]
    fn fnv_256_with_lang(data: &[u8], lang: super::LangCode) -> [u8; 32] {
        let lo = fnv_128_with_lang(data, lang);
        let mut sep = data.to_vec();
        sep.push(0xAA);
        let hi = fnv_128_with_lang(&sep, lang);
        let mut out = [0u8; 32];
        out[..16].copy_from_slice(&lo);
        out[16..].copy_from_slice(&hi);
        out
    }
}

