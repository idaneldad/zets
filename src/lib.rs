// META-GRAPH — system describes itself
// ============================================================================

pub mod meta_graph {
    //! The 11th sphere: system rules live in the graph.
    //!
    //! System synsets are pre-seeded in the reserved ID range 0..999.
    //! User content starts at SynsetId::USER_CONTENT_START (10_000).
    //!
    //! This gives free introspection: querying `outgoing(LANGUAGE_REGISTRY)`
    //! returns all languages as regular graph edges.

    use super::{SynsetId, SystemSynset, SYSTEM_SYNSETS, LANGUAGES};

    /// Look up a system synset by its canonical key.
    /// Example: `by_key("he")` returns the synset for Hebrew.
    pub fn by_key(key: &str) -> Option<SynsetId> {
        SYSTEM_SYNSETS
            .iter()
            .find(|s| s.key == key)
            .map(|s| SynsetId(s.id))
    }

    /// Look up a system synset by category.
    pub fn by_category(category: &str) -> Vec<SynsetId> {
        SYSTEM_SYNSETS
            .iter()
            .filter(|s| s.category == category)
            .map(|s| SynsetId(s.id))
            .collect()
    }

    /// List the system synsets that belong to a category.
    pub fn category_entries(category: &str) -> Vec<&'static SystemSynset> {
        SYSTEM_SYNSETS
            .iter()
            .filter(|s| s.category == category)
            .collect()
    }

    /// All language codes that have a corresponding system synset.
    pub fn known_language_codes() -> Vec<&'static str> {
        LANGUAGES.iter().map(|l| l.code).collect()
    }
}

