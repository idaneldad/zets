//! ZETS library — Deterministic knowledge graph engine for edge devices.
//!
//! # Architecture
//!
//! Single binary. Zero runtime dependencies. Data stored as TSV in `data/`,
//! baked at compile time via `build.rs` into `$OUT_DIR/generated.rs`.
//!
//! The system describes itself in its own graph: reserved synset IDs 0..999
//! hold system concepts (languages, relations, permissions). IDs 10000+ hold
//! user-facing content. This is the "homoiconic 11th sphere" — meta-rules
//! live in the graph like any other node.
//!
//! # Current coverage (Week 1-2)
//!
//! - `LangCode`: ISO 639-1 language identifier (2 bytes, Copy)
//! - `unp`: Unified Normalization Pipeline (NFC stub, trim, collapse, lang-specific, lemmatize)
//! - `hebrew`: niqud detection, final-form normalization, rules-based stemmer
//! - `meta_graph`: system synsets pre-seeded at EdgeStore creation
//! - `edge_store`: SOA columnar graph, 10 bytes/edge, binary serialization
//! - `bloom`: custom Bloom filter, FNV-1a + Kirsch-Mitzenmacher
//!
//! # Data loading strategy
//!
//! - **Core tables** (prefixes, suffixes, relations, system synsets): embedded
//!   at compile time. Always present, zero runtime I/O.
//! - **Language packs** (surface→lemma dictionaries, wikipedia content):
//!   downloaded on demand when user activates a language. Stored at
//!   `~/.zets/packs/{code}_v{n}.pack`.

#![forbid(unsafe_code)]
