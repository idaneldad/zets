pub mod edge_store {
    //! Columnar edge storage.
    //!
    //! Layout (10 bytes per edge):
    //! - `sources`:    3 bytes (u24)
    //! - `targets`:    3 bytes (u24)
    //! - `rel_weight`: 1 byte (5 bits relation + 3 bits weight)
    //! - `provenance`: 3 bytes (u24)
    //!
    //! SOA beats AOS because graph walks touch mostly source/target, so
    //! cache line utilization is ~2.3× higher.

    use super::{SynsetId, SYSTEM_SYNSETS};
    use std::io::{self, Read, Write};
    

    /// 27 relation types (fits in 5 bits, MAX=26).
    #[derive(Debug, Clone, Copy, PartialEq, Eq)]
    #[repr(u8)]
    pub enum Relation {
        IsA = 0,
        HasPart = 1,
        MemberOf = 2,
        SameAs = 3,
        NearSynonym = 4,
        Causes = 5,
        Enables = 6,
        Prevents = 7,
        Precedes = 8,
        Follows = 9,
        UsedFor = 10,
        Requires = 11,
        Produces = 12,
        Satisfies = 13,
        Implies = 14,
        Contradicts = 15,
        DefinedBy = 16,
        ExampleOf = 17,
        LocatedIn = 18,
        Contains = 19,
        AdjacentTo = 20,
        AgentOf = 21,
        PatientOf = 22,
        InstrumentOf = 23,
        DerivedFrom = 24,
        VariationOf = 25,
        OpposedTo = 26,
        TextNext = 27,
        AppearsIn = 28,
        Cites = 29,
    }

    impl Relation {
        pub const MAX_VALUE: u8 = 29;

        #[inline]
        pub const fn from_u8(v: u8) -> Option<Self> {
            match v {
                0 => Some(Self::IsA), 1 => Some(Self::HasPart),
                2 => Some(Self::MemberOf), 3 => Some(Self::SameAs),
                4 => Some(Self::NearSynonym), 5 => Some(Self::Causes),
                6 => Some(Self::Enables), 7 => Some(Self::Prevents),
                8 => Some(Self::Precedes), 9 => Some(Self::Follows),
                10 => Some(Self::UsedFor), 11 => Some(Self::Requires),
                12 => Some(Self::Produces), 13 => Some(Self::Satisfies),
                14 => Some(Self::Implies), 15 => Some(Self::Contradicts),
                16 => Some(Self::DefinedBy), 17 => Some(Self::ExampleOf),
                18 => Some(Self::LocatedIn), 19 => Some(Self::Contains),
                20 => Some(Self::AdjacentTo), 21 => Some(Self::AgentOf),
                22 => Some(Self::PatientOf), 23 => Some(Self::InstrumentOf),
                24 => Some(Self::DerivedFrom), 25 => Some(Self::VariationOf),
                26 => Some(Self::OpposedTo),
                27 => Some(Self::TextNext),
                28 => Some(Self::AppearsIn),
                29 => Some(Self::Cites),
                _ => None,
            }
        }

        /// Human-readable name, from the compile-time table.
        #[inline]
        pub fn name(self) -> &'static str {
            super::RELATION_NAMES[self as usize]
        }

        /// Inverse relation if defined, else None.
        #[inline]
        pub fn inverse(self) -> Option<Self> {
            let inv = super::RELATION_INVERSES[self as usize];
            if inv == 255 { None } else { Self::from_u8(inv) }
        }

        /// The system synset representing this relation itself (homoiconic).
        /// Example: `Relation::IsA.as_synset()` returns SynsetId(30).
        #[inline]
        pub const fn as_synset(self) -> SynsetId {
            SynsetId(30 + self as u32)
        }
    }

    /// A complete edge in user-facing form.
    #[derive(Debug, Clone, Copy, PartialEq, Eq)]
    pub struct Edge {
        pub source: SynsetId,
        pub target: SynsetId,
        pub relation: Relation,
        pub weight: u8,        // 0..=7 (3 bits)
        pub provenance: u32,   // u24 index into ProvenanceTable
    }

    #[inline]
    const fn pack_rel_weight(rel: Relation, weight: u8) -> u8 {
        // weight in top 3 bits, relation in bottom 5 bits
        (rel as u8) | (weight << 5)
    }

    #[inline]
    const fn unpack_rel_weight(byte: u8) -> (Relation, u8) {
        let rel_bits = byte & 0b0001_1111;
        let weight = byte >> 5;
        let rel = match Relation::from_u8(rel_bits) {
            Some(r) => r,
            None => Relation::IsA, // fallback for corrupt data
        };
        (rel, weight)
    }

    #[inline]
    fn write_u24(buf: &mut Vec<u8>, value: u32) {
        debug_assert!(value <= SynsetId::MAX, "u24 overflow: {value}");
        buf.push((value & 0xFF) as u8);
        buf.push(((value >> 8) & 0xFF) as u8);
        buf.push(((value >> 16) & 0xFF) as u8);
    }

    #[inline]
    const fn read_u24(buf: &[u8], i: usize) -> u32 {
        let off = i * 3;
        (buf[off] as u32) | ((buf[off + 1] as u32) << 8) | ((buf[off + 2] as u32) << 16)
    }

    /// In-memory columnar edge store.
    #[derive(Debug, Default, Clone)]
    pub struct EdgeStore {
        sources: Vec<u8>,
        targets: Vec<u8>,
        rel_weight: Vec<u8>,
        provenance: Vec<u8>,
    }

    impl EdgeStore {
        const MAGIC: &'static [u8] = b"ZEDG";
        const VERSION: u8 = 1;

        #[inline]
        #[must_use]
        pub fn new() -> Self {
            Self::default()
        }

        /// Create with pre-allocated capacity for `n` edges.
        #[must_use]
        pub fn with_capacity(n: usize) -> Self {
            Self {
                sources: Vec::with_capacity(n * 3),
                targets: Vec::with_capacity(n * 3),
                rel_weight: Vec::with_capacity(n),
                provenance: Vec::with_capacity(n * 3),
            }
        }

        /// Create with meta-graph pre-seeded: system synsets linked into their
        /// registry nodes. The graph describes itself from step zero.
        #[must_use]
        pub fn new_with_meta() -> Self {
            let mut store = Self::with_capacity(SYSTEM_SYNSETS.len());

            // Link each system synset to its category parent via MEMBER_OF.
            for synset in SYSTEM_SYNSETS {
                let parent_id = match synset.category {
                    "language" => SynsetId::LANGUAGE_REGISTRY,
                    "relation" => SynsetId::RELATION_REGISTRY,
                    "permission" => SynsetId::PERMISSION_REGISTRY,
                    "status" => SynsetId::STATUS_REGISTRY,
                    "system" => SynsetId::SYSTEM_ROOT,
                    _ => continue,
                };
                if synset.id == parent_id.0 {
                    continue; // don't self-link registries
                }
                store.push(Edge {
                    source: SynsetId(synset.id),
                    target: parent_id,
                    relation: Relation::MemberOf,
                    weight: 7, // maximum confidence for system facts
                    provenance: 0, // provenance slot 0 = "system bootstrap"
                });
            }
            store
        }

        #[inline]
        pub fn len(&self) -> usize {
            self.rel_weight.len()
        }

        #[inline]
        pub fn is_empty(&self) -> bool {
            self.rel_weight.is_empty()
        }

        /// Bytes used by the store (excluding Vec overhead).
        #[inline]
        pub fn bytes_used(&self) -> usize {
            self.sources.len() + self.targets.len()
                + self.rel_weight.len() + self.provenance.len()
        }

        /// Append an edge. Returns its index.
        pub fn push(&mut self, edge: Edge) -> usize {
            let idx = self.len();
            write_u24(&mut self.sources, edge.source.0);
            write_u24(&mut self.targets, edge.target.0);
            self.rel_weight.push(pack_rel_weight(edge.relation, edge.weight));
            write_u24(&mut self.provenance, edge.provenance);
            idx
        }

        /// Read edge at index. `None` if out of bounds.
        #[inline]
        pub fn get(&self, i: usize) -> Option<Edge> {
            if i >= self.len() {
                return None;
            }
            let source = SynsetId(read_u24(&self.sources, i));
            let target = SynsetId(read_u24(&self.targets, i));
            let (relation, weight) = unpack_rel_weight(self.rel_weight[i]);
            let provenance = read_u24(&self.provenance, i);
            Some(Edge { source, target, relation, weight, provenance })
        }

        pub fn iter(&self) -> EdgeIter<'_> {
            EdgeIter { store: self, pos: 0 }
        }

        /// Edges where `source == src`. Linear scan; Week 3 adds bitmap index.
        pub fn outgoing(&self, src: SynsetId) -> Vec<Edge> {
            let mut out = Vec::new();
            let n = self.len();
            for i in 0..n {
                if read_u24(&self.sources, i) == src.0 {
                    if let Some(e) = self.get(i) {
                        out.push(e);
                    }
                }
            }
            out
        }

        /// Edges where `target == tgt`.
        pub fn incoming(&self, tgt: SynsetId) -> Vec<Edge> {
            let mut out = Vec::new();
            let n = self.len();
            for i in 0..n {
                if read_u24(&self.targets, i) == tgt.0 {
                    if let Some(e) = self.get(i) {
                        out.push(e);
                    }
                }
            }
            out
        }

        /// Serialize: ZEDG magic + version + u64 count + 4 columns.
        pub fn write_to<W: Write>(&self, w: &mut W) -> io::Result<()> {
            w.write_all(Self::MAGIC)?;
            w.write_all(&[Self::VERSION])?;
            let count = self.len() as u64;
            w.write_all(&count.to_le_bytes())?;
            w.write_all(&self.sources)?;
            w.write_all(&self.targets)?;
            w.write_all(&self.rel_weight)?;
            w.write_all(&self.provenance)?;
            Ok(())
        }

        /// Deserialize from reader.
        pub fn read_from<R: Read>(r: &mut R) -> io::Result<Self> {
            let mut magic = [0u8; 4];
            r.read_exact(&mut magic)?;
            if magic != *Self::MAGIC {
                return Err(io::Error::new(io::ErrorKind::InvalidData,
                    format!("bad magic: {magic:?}")));
            }
            let mut ver = [0u8; 1];
            r.read_exact(&mut ver)?;
            if ver[0] != Self::VERSION {
                return Err(io::Error::new(io::ErrorKind::InvalidData,
                    format!("unsupported version: {}", ver[0])));
            }
            let mut cb = [0u8; 8];
            r.read_exact(&mut cb)?;
            let count = u64::from_le_bytes(cb) as usize;

            let mut store = Self::with_capacity(count);
            store.sources.resize(count * 3, 0);
            store.targets.resize(count * 3, 0);
            store.rel_weight.resize(count, 0);
            store.provenance.resize(count * 3, 0);

            r.read_exact(&mut store.sources)?;
            r.read_exact(&mut store.targets)?;
            r.read_exact(&mut store.rel_weight)?;
            r.read_exact(&mut store.provenance)?;
            Ok(store)
        }
    }

    pub struct EdgeIter<'a> {
        store: &'a EdgeStore,
        pos: usize,
    }

    impl Iterator for EdgeIter<'_> {
        type Item = Edge;
        #[inline]
        fn next(&mut self) -> Option<Edge> {
            let e = self.store.get(self.pos)?;
            self.pos += 1;
            Some(e)
        }
    }

    // ========================================================================
    // ADJACENCY INDEX — sorted-edge binary search, O(log N) lookups
    // ========================================================================

    /// Auxiliary index for O(log N) lookups by source or target.
    ///
    /// Built on demand from an `EdgeStore`. Stores two permutations of edge
    /// indices: one sorted by source, one sorted by target. Each permutation
    /// is a `Vec<u32>` — 4 bytes per edge per direction, 8 bytes total overhead.
    ///
    /// # When to use
    ///
    /// Linear scan via `outgoing()`/`incoming()` is fine up to ~1M edges
    /// (measured: ~1ms per query). Above that, scan time dominates: 5M edges
    /// needs ~5ms per query, and multi-hop walks (depth 3, fanout 5) blow
    /// through the 100ms target.
    ///
    /// Build the index once after bulk loading, then use `outgoing_indexed()`
    /// and `incoming_indexed()` for fast walks.
    ///
    /// # Memory cost
    ///
    /// Index of 10M edges = 80MB RAM (2 × 4 bytes × 10M). For devices like
    /// Pi 5 with 8GB, this is acceptable. For phones, might want to index
    /// only frequently-walked direction.
    ///
    /// # Complexity
    ///
    /// - Build: O(N log N) — two sorts
    /// - Lookup: O(log N + K) — binary search + K matching edges
    ///
    /// # Rebuild policy
    ///
    /// Index is static. After `store.push()`, the index is stale. The
    /// maintenance cycle (idle-aware compaction, SPEC V6.1 §V) rebuilds it.
    #[derive(Debug, Clone)]
    pub struct AdjacencyIndex {
        /// Edge indices sorted by source synset ID (ascending).
        /// `by_source[i]` is the original position in the EdgeStore.
        by_source: Vec<u32>,
        /// Edge indices sorted by target synset ID (ascending).
        by_target: Vec<u32>,
    }

    impl AdjacencyIndex {
        /// Build a fresh index from the current state of an `EdgeStore`.
        ///
        /// Cost: O(N log N). For 1M edges: ~80ms on Oracle x86_64.
        #[must_use]
        pub fn build(store: &EdgeStore) -> Self {
            let n = store.len();
            let mut by_source: Vec<u32> = (0..u32::try_from(n).unwrap_or(u32::MAX)).collect();
            let mut by_target: Vec<u32> = by_source.clone();

            // Closures that read directly from packed columns.
            // No Vec<Edge> materialization — work on indices only.
            by_source.sort_unstable_by_key(|&i| read_u24(&store.sources, i as usize));
            by_target.sort_unstable_by_key(|&i| read_u24(&store.targets, i as usize));

            Self { by_source, by_target }
        }

        /// Bytes used by the index itself (excluding Vec overhead).
        ///
        /// Each index is `4 * edge_count` bytes; we have two indices.
        #[inline]
        #[must_use]
        pub fn bytes_used(&self) -> usize {
            (self.by_source.len() + self.by_target.len()) * 4
        }

        #[inline]
        #[must_use]
        pub fn edge_count(&self) -> usize {
            self.by_source.len()
        }

        /// Find the range `[lo, hi)` of `by_source` where the source equals `src`.
        /// Uses `partition_point` (binary search) twice — O(log N) total.
        fn source_range(&self, store: &EdgeStore, src: u32) -> (usize, usize) {
            let lo = self.by_source.partition_point(|&i| {
                read_u24(&store.sources, i as usize) < src
            });
            let hi = self.by_source.partition_point(|&i| {
                read_u24(&store.sources, i as usize) <= src
            });
            (lo, hi)
        }

        /// Find the range `[lo, hi)` of `by_target` where the target equals `tgt`.
        fn target_range(&self, store: &EdgeStore, tgt: u32) -> (usize, usize) {
            let lo = self.by_target.partition_point(|&i| {
                read_u24(&store.targets, i as usize) < tgt
            });
            let hi = self.by_target.partition_point(|&i| {
                read_u24(&store.targets, i as usize) <= tgt
            });
            (lo, hi)
        }

        /// Edges where `source == src`. O(log N + K).
        #[must_use]
        pub fn outgoing(&self, store: &EdgeStore, src: SynsetId) -> Vec<Edge> {
            let (lo, hi) = self.source_range(store, src.0);
            let mut out = Vec::with_capacity(hi - lo);
            for &edge_idx in &self.by_source[lo..hi] {
                if let Some(e) = store.get(edge_idx as usize) {
                    out.push(e);
                }
            }
            out
        }

        /// Edges where `target == tgt`. O(log N + K).
        #[must_use]
        pub fn incoming(&self, store: &EdgeStore, tgt: SynsetId) -> Vec<Edge> {
            let (lo, hi) = self.target_range(store, tgt.0);
            let mut out = Vec::with_capacity(hi - lo);
            for &edge_idx in &self.by_target[lo..hi] {
                if let Some(e) = store.get(edge_idx as usize) {
                    out.push(e);
                }
            }
            out
        }

        /// Count outgoing edges without materializing them. O(log N).
        #[must_use]
        pub fn outgoing_count(&self, store: &EdgeStore, src: SynsetId) -> usize {
            let (lo, hi) = self.source_range(store, src.0);
            hi - lo
        }

        /// Count incoming edges without materializing them. O(log N).
        #[must_use]
        pub fn incoming_count(&self, store: &EdgeStore, tgt: SynsetId) -> usize {
            let (lo, hi) = self.target_range(store, tgt.0);
            hi - lo
        }

        /// Find all distinct source synsets with at least one outgoing edge.
        /// O(N) first call, could be cached. Useful for graph statistics.
        #[must_use]
        pub fn distinct_sources(&self, store: &EdgeStore) -> Vec<SynsetId> {
            let mut out = Vec::new();
            let mut last: Option<u32> = None;
            for &i in &self.by_source {
                let s = read_u24(&store.sources, i as usize);
                if last != Some(s) {
                    out.push(SynsetId(s));
                    last = Some(s);
                }
            }
            out
        }
    }

}


