// ============================================================================

pub mod bloom {
    //! Custom Bloom filter — FNV-1a with Kirsch-Mitzenmacher double-hashing.
    //!
    //! Default: 8 MiB bitmap, 7 hashes. Good for ~1M items, <1% FP rate.

    pub const DEFAULT_BITS: usize = 8 * 1024 * 1024 * 8;
    pub const DEFAULT_HASHES: u8 = 7;

    pub struct BloomFilter {
        bits: Vec<u8>,
        num_bits: usize,
        num_hashes: u8,
    }

    impl BloomFilter {
        #[must_use]
        pub fn new(num_bits: usize, num_hashes: u8) -> Self {
            Self {
                bits: vec![0u8; num_bits.div_ceil(8)],
                num_bits,
                num_hashes,
            }
        }

        #[must_use]
        pub fn default_size() -> Self {
            Self::new(DEFAULT_BITS, DEFAULT_HASHES)
        }

        #[inline]
        pub fn bytes_used(&self) -> usize {
            self.bits.len()
        }

        pub fn insert(&mut self, key: &[u8]) {
            let (h1, h2) = Self::double_hash(key);
            for i in 0..self.num_hashes {
                let idx = self.bit_index(h1, h2, i);
                self.set_bit(idx);
            }
        }

        #[inline]
        pub fn might_contain(&self, key: &[u8]) -> bool {
            let (h1, h2) = Self::double_hash(key);
            for i in 0..self.num_hashes {
                let idx = self.bit_index(h1, h2, i);
                if !self.get_bit(idx) {
                    return false;
                }
            }
            true
        }

        pub fn bits_set(&self) -> usize {
            self.bits.iter().map(|b| b.count_ones() as usize).sum()
        }

        pub fn estimated_fp_rate(&self) -> f64 {
            let ratio = self.bits_set() as f64 / self.num_bits as f64;
            ratio.powi(i32::from(self.num_hashes))
        }

        #[inline]
        fn double_hash(key: &[u8]) -> (u64, u64) {
            (fnv1a_64(key, 0xcbf2_9ce4_8422_2325),
             fnv1a_64(key, 0x8422_2325_cbf2_9ce4))
        }

        #[inline]
        fn bit_index(&self, h1: u64, h2: u64, i: u8) -> usize {
            let combined = h1.wrapping_add(u64::from(i).wrapping_mul(h2));
            (combined as usize) % self.num_bits
        }

        #[inline]
        fn set_bit(&mut self, idx: usize) {
            self.bits[idx / 8] |= 1 << (idx % 8);
        }

        #[inline]
        fn get_bit(&self, idx: usize) -> bool {
            (self.bits[idx / 8] >> (idx % 8)) & 1 == 1
        }
    }

    #[inline]
    fn fnv1a_64(data: &[u8], seed: u64) -> u64 {
        const PRIME: u64 = 0x100_0000_01b3;
        let mut hash = seed;
        for &b in data {
            hash ^= u64::from(b);
            hash = hash.wrapping_mul(PRIME);
        }
        hash
    }
}

