// DOCUMENT — text as a sequence of tokens grounded in provenance
// ============================================================================

pub mod document {
    //! Text as a first-class graph structure.
    //!
    //! A document is not a flat bag of facts — it is an ordered sequence of
    //! tokens, each linked to the document it appears in. This lets the graph
    //! answer questions like:
    //!
    //! - "What word follows X in document D?" → `outgoing(X, TextNext, in D)`
    //! - "Where does X appear?" → `outgoing(X, AppearsIn)`
    //! - "What does document D reference?" → `outgoing(D, Cites)`
    //!
    //! # Layout
    //!
    //! For a document D and tokens `[t_0, t_1, t_2, ...]`:
    //!
    //! ```text
    //! t_0 --TEXT_NEXT(provenance=pos0)--> t_1
    //! t_1 --TEXT_NEXT(provenance=pos1)--> t_2
    //! ...
    //! t_0 --APPEARS_IN(provenance=pos0)--> D
    //! t_1 --APPEARS_IN(provenance=pos1)--> D
    //! ...
    //! ```
    //!
    //! Position is carried in the `provenance` field (u24, up to 16M positions
    //! per document — more than enough for a book).
    //!
    //! # Document synset IDs
    //!
    //! Documents live in the concept space (1_000_000+) so they don't collide
    //! with surface-form synsets (10_000..999_999).

    use super::edge_store::{Edge, EdgeStore, Relation};
    use super::SynsetId;

    /// First synset ID reserved for documents.
    pub const DOCUMENT_ID_START: u32 = 1_000_000;

    /// Document metadata (held in a separate registry).
    #[derive(Debug, Clone)]
    pub struct DocumentMeta {
        pub id: SynsetId,
        pub title: String,
        pub source_uri: String,
        pub token_count: u32,
    }

    /// Allocator for document IDs — produces monotonically increasing IDs
    /// starting from `DOCUMENT_ID_START`.
    #[derive(Debug)]
    pub struct DocumentIdAllocator {
        next: u32,
    }

    impl Default for DocumentIdAllocator {
        fn default() -> Self {
            Self { next: DOCUMENT_ID_START }
        }
    }

    impl DocumentIdAllocator {
        #[must_use]
        pub fn new() -> Self {
            Self::default()
        }

        /// Allocate the next document ID.
        #[inline]
        pub fn allocate(&mut self) -> SynsetId {
            let id = SynsetId(self.next);
            self.next += 1;
            id
        }

        #[inline]
        #[must_use]
        pub const fn count(&self) -> u32 {
            self.next - DOCUMENT_ID_START
        }
    }

    /// Add a token sequence to a graph.
    ///
    /// Creates two kinds of edges for each token at position `i`:
    /// 1. `tokens[i] --TEXT_NEXT--> tokens[i+1]` with provenance = `i`
    /// 2. `tokens[i] --APPEARS_IN--> doc` with provenance = `i`
    ///
    /// Returns the number of edges added. For N tokens this is `(N-1) + N = 2N-1`.
    ///
    /// # Parameters
    /// - `store`: mutable graph to append into
    /// - `doc`: document synset ID (from `DocumentIdAllocator`)
    /// - `tokens`: sequence of token synset IDs in document order
    ///
    /// # Example
    ///
    /// ```ignore
    /// let mut store = EdgeStore::new();
    /// let mut alloc = DocumentIdAllocator::new();
    /// let doc = alloc.allocate();
    /// let tokens = [SynsetId(10_001), SynsetId(10_002), SynsetId(10_003)];
    /// add_sequence(&mut store, doc, &tokens);
    /// // Now:
    /// //   outgoing(10_001, TEXT_NEXT) = [10_002] at pos 0
    /// //   outgoing(10_002, TEXT_NEXT) = [10_003] at pos 1
    /// //   outgoing(10_001, APPEARS_IN) = [doc] at pos 0
    /// ```
    pub fn add_sequence(store: &mut EdgeStore, doc: SynsetId, tokens: &[SynsetId]) -> usize {
        if tokens.is_empty() {
            return 0;
        }
        let mut count = 0;

        // Sequence edges: t_i --TEXT_NEXT--> t_{i+1}
        for i in 0..tokens.len().saturating_sub(1) {
            store.push(Edge {
                source: tokens[i],
                target: tokens[i + 1],
                relation: Relation::TextNext,
                weight: 7,
                provenance: u32::try_from(i).unwrap_or(u32::MAX),
            });
            count += 1;
        }

        // Membership edges: t_i --APPEARS_IN--> doc
        for (i, &tok) in tokens.iter().enumerate() {
            store.push(Edge {
                source: tok,
                target: doc,
                relation: Relation::AppearsIn,
                weight: 7,
                provenance: u32::try_from(i).unwrap_or(u32::MAX),
            });
            count += 1;
        }

        count
    }

    /// Record that document `from` cites document `to` (reference / quotation /
    /// hyperlink). `weight` represents confidence of the citation relationship
    /// (0-7); use 7 for direct explicit citations, lower for inferred.
    #[inline]
    pub fn add_citation(store: &mut EdgeStore, from: SynsetId, to: SynsetId, weight: u8) {
        store.push(Edge {
            source: from,
            target: to,
            relation: Relation::Cites,
            weight,
            provenance: 0,
        });
    }

    /// Reconstruct the ordered token sequence of a document by walking
    /// `APPEARS_IN` edges sorted by position (provenance).
    ///
    /// This is O(N) in total edges — Week 3 will add an index for O(log N).
    /// Use for verification, not hot paths.
    #[must_use]
    pub fn reconstruct_sequence(store: &EdgeStore, doc: SynsetId) -> Vec<(u32, SynsetId)> {
        let mut tokens: Vec<(u32, SynsetId)> = store
            .incoming(doc)
            .into_iter()
            .filter(|e| e.relation == Relation::AppearsIn)
            .map(|e| (e.provenance, e.source))
            .collect();
        tokens.sort_by_key(|&(pos, _)| pos);
        tokens
    }

    /// What word follows `token` in any document? Returns list of (doc-position,
    /// next-token) pairs across all documents where `token` appears.
    #[must_use]
    pub fn next_tokens(store: &EdgeStore, token: SynsetId) -> Vec<(u32, SynsetId)> {
        store
            .outgoing(token)
            .into_iter()
            .filter(|e| e.relation == Relation::TextNext)
            .map(|e| (e.provenance, e.target))
            .collect()
    }

    /// What documents does `doc` cite?
    #[must_use]
    pub fn citations(store: &EdgeStore, doc: SynsetId) -> Vec<SynsetId> {
        store
            .outgoing(doc)
            .into_iter()
            .filter(|e| e.relation == Relation::Cites)
            .map(|e| e.target)
            .collect()
    }

    /// What documents cite `doc`? (reverse citation)
    #[must_use]
    pub fn cited_by(store: &EdgeStore, doc: SynsetId) -> Vec<SynsetId> {
        store
            .incoming(doc)
            .into_iter()
            .filter(|e| e.relation == Relation::Cites)
            .map(|e| e.source)
            .collect()
    }
}

